void UTFT::_hw_special_init(){}

void UTFT::LCD_Writ_Bus(char VH,char VL, byte mode)
{
	if(mode == 16) {
		PORTA = VH;
		PORTC = VL;
		
		pulse_low(P_WR, B_WR);
	}	
}

void UTFT::_set_direction_registers(byte mode)
{
	if(mode == 16) {
		DDRA = 0xff;
		DDRC = 0xff;
	}
}

void UTFT::_fast_fill_16(int ch, int cl, long pix)
{
	long blocks;

	PORTA = ch;
	PORTC = cl;

	blocks = pix/16;
	for (int i=0; i<blocks; i++)
	{
		pulse_low(P_WR, B_WR);
		pulse_low(P_WR, B_WR);
		pulse_low(P_WR, B_WR);
		pulse_low(P_WR, B_WR);
		pulse_low(P_WR, B_WR);
		pulse_low(P_WR, B_WR);
		pulse_low(P_WR, B_WR);
		pulse_low(P_WR, B_WR);
		pulse_low(P_WR, B_WR);
		pulse_low(P_WR, B_WR);
		pulse_low(P_WR, B_WR);
		pulse_low(P_WR, B_WR);
		pulse_low(P_WR, B_WR);
		pulse_low(P_WR, B_WR);
		pulse_low(P_WR, B_WR);
		pulse_low(P_WR, B_WR);
	}
	if ((pix % 16) != 0)
		for (int i=0; i<(pix % 16)+1; i++)
		{
			pulse_low(P_WR, B_WR);
		}
}

